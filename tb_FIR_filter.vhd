        for i in 0 to 255 loop
            s_axis_fir_tdata <= to_signed(SINE_PASS(i), 16);
            if (i = 255) then s_axis_fir_tlast <= '1'; end if;
            wait for CLK_PERIOD;
        end loop;
        s_axis_fir_tlast <= '0';
        
        -- boundary
        s_axis_fir_tvalid <= '0';
        wait for 10 * CLK_PERIOD;
        
